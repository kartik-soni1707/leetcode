# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def flatten(self, root: Optional[TreeNode]) -> None:
        """
        Do not return anything, modify root in-place instead.
        """

        def explorer(node):
            if not node:
                return None
            lt=explorer(node.left)
            rt=explorer(node.right)
            if node.left:
                temp=node.left
                node.left=None
                lt.right=node.right
                node.right=temp
            return rt or lt or node
        explorer(root)
        return root
